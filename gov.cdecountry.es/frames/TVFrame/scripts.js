var player;

/** Listen to Youtube API */
function onYouTubeIframeAPIReady() {
    player = new YT.Player('video-placeholder', {
        videoId: setVideoByNow(false),
        playerVars: {
            cc_load_policy: 3,
            autoplay: 1,
            controls: 0,
            color: 'white',
            showinfo: 0,
            playlist: (setVideoByNow(false))
        },
        events: {
            onReady: initialize
        }
    });
}

/** Start intervals & video player */
function initialize() {
    setTimeout(function() {
        player.playVideo();
    }, 1000);

    setInterval(iconize, 1000);
    setInterval(bucle, 1000);
}

/** Set current video and current play minute */
function bucle(){
    setVideoByNow(true);
}

/** Check if player is mobile */
function isMobile() {
    return ('ontouchstart' in document.documentElement);
}

/** Resize & re-pos the tv logo */
function iconize() {
    var xhw = [document.getElementById("overlay").clientWidth, document.getElementById("overlay").clientHeight];
    xhw[2] = (xhw[1] * 1.77);
    xhw[3] = (xhw[0] * 0.5625);
    var result = (xhw[0] - xhw[2]) / 2;
    var resultB = (xhw[1] - xhw[3]) / 2;
    if (result < 0) result = 0;
    if (resultB < 0) resultB = 0;
    document.getElementsByClassName("logo")[0].style.marginLeft = result;
    document.getElementsByClassName("logo")[0].style.marginTop = resultB;

    document.getElementById("video-placeholder").style.width = xhw[0];
    document.getElementById("video-placeholder").style.height = xhw[1];

    if (player.getPlayerState() != 1) {
        document.getElementById("video-placeholder").click();
        player.playVideo();
        console.log("Played!");
    }
}

/** Syncronized video funcion */
function setVideoByNow(toggle){

    // Start environment
    var videoId,minute;
    var total = 354886.5628185938;
    var millis = 1526657094.3718;
    var now = Date.now()/1000;
    var time = now-millis;
    var cycle = time/total;
    var newtime = (cycle-parseInt(cycle))*total;
    var proccess_time = 0;

    // Obtain current video from map
    for(var a=0;a<videoMap.length;a++){
        proccess_time+=videoMap[a][1];
        if(proccess_time >= newtime){
            minute = videoMap[a][1]-(proccess_time-newtime);
            videoId=videoMap[a][0];
            console.log("Vid: "+videoId+" : "+minute);
            break;
        }
    }
    
    // Return current video id when var:toggle is set to false
    if(!toggle) return videoId;

    // Set current video and current minute.
    if(videoId != player.getVideoData().video_id) player.loadVideoById(videoId);
    if(videoId == player.getVideoData().video_id){
       var mi = (minute-5);
        var ma = (minute+5);
        var mn = player.getCurrentTime();

        var o1 = mn>mi;
        var o2 = ma>mn;

        if(!(o1 && o2)) player.seekTo(minute);
    }

    // Finnaly return the videoId
    return videoId;
}

